# Define string
msg = 'Hello'

# Print string using different methods
print("msg:", msg)
print("msg.lower():", msg.lower())
print("msg.upper():", msg.upper())
print("msg.capitalise():", msg.capitalize())
print("msg.title():", msg.title())
print("msg.swapcase():", msg.swapcase())
print("len(msg):", len(msg))
print("msg.count('l'):", msg.count('l'))
print("msg.find('l'):", msg.find('l'))
print("msg.isdigit():", msg.isdigit())
print("msg.isalpha():", msg.isalpha())
print("msg*3:", msg*3)